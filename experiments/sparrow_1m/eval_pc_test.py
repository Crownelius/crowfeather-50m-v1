"""Phase E Phase 1.E — eval iter37 (PC-trained Sparrow) on 3-digit multiplication.

Custom generation loop because:
  1. Sparrow-PC was trained with coupled position_ids; we must pass coupled
     IDs to forward() at inference, not HF's default sequential.
  2. Sparrow-PC emits the result LSB-first; we must reverse before scoring
     against MSB-first expected answers (matching iter6a/iter15 conventions).

This is a Phase 1 sanity check: does iter37 saturate 3-digit mul (vs iter6a's
76% cap)? If yes -> Phase 1 success. Length-generalization test on 6-digit
mul follows in eval_pc_lengthgen.py.

Usage:
    python eval_pc_test.py \\
        --sparrow E:/sparrow/iter37/trained/final \\
        --task mul --digits 3 --n 50 \\
        --report E:/sparrow/iter37/trained/final/eval_pc_mul_3d.json
"""
import argparse
import json
import os
import random
import sys
import time

import torch

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from bytes_tok import encode, decode  # noqa: E402
from gen_arith import gen_problem, compute_pc_position_ids  # noqa: E402


SPACE_ID = 32
NEWLINE_ID = 10
EQUALS_ID = 61


def is_digit_byte(b: int) -> bool:
    return 48 <= b <= 57


def build_pc_prompt(a: int, b: int, op_char: str, digits: int):
    """Construct the byte sequence + coupled position_ids for the inference prompt:
        '<a per-digit MSB-first> <op> <b per-digit MSB-first> = '
    Returns (input_ids: list[int], position_ids: list[int], n_digit_a: int, n_digit_b: int).
    """
    a_digits = [int(c) for c in str(a)]
    b_digits = [int(c) for c in str(b)]
    n_a, n_b = len(a_digits), len(b_digits)
    op_pos = 1 + max(n_a, n_b)  # start=1

    # Build the byte sequence and position-IDs in lockstep.
    bytes_out = []
    positions = []

    # Operand A — MSB-first, positions [start + n_a-1, ..., start]
    for i, d in enumerate(a_digits):
        if i > 0:
            bytes_out.append(SPACE_ID)
            positions.append(positions[-1])  # space inherits left
        bytes_out.append(48 + d)
        positions.append(1 + (n_a - 1 - i))

    # Space, op, space
    bytes_out.append(SPACE_ID); positions.append(positions[-1])
    bytes_out.append(ord(op_char)); positions.append(op_pos)
    bytes_out.append(SPACE_ID); positions.append(op_pos)

    # Operand B
    for i, d in enumerate(b_digits):
        if i > 0:
            bytes_out.append(SPACE_ID)
            positions.append(positions[-1])
        bytes_out.append(48 + d)
        positions.append(1 + (n_b - 1 - i))

    # Space, =, space — prompt ends after the trailing space (so first generated
    # byte is the result LSB digit)
    bytes_out.append(SPACE_ID); positions.append(positions[-1])
    bytes_out.append(EQUALS_ID); positions.append(op_pos)
    bytes_out.append(SPACE_ID); positions.append(op_pos)

    return bytes_out, positions, n_a, n_b


def generate_pc(model, prompt_ids: list, prompt_positions: list, device: str,
                max_new: int = 32):
    """Custom autoregressive decode for a PC-trained Sparrow.

    Tracks how many result digits have been emitted so far; the next digit's
    position is `start + n_result_digit_emitted` (with start=1).

    Returns the generated bytes (excluding prompt, excluding final newline).
    """
    input_ids = torch.tensor([prompt_ids], dtype=torch.long, device=device)
    position_ids = torch.tensor([prompt_positions], dtype=torch.long, device=device)
    generated = []
    n_result_digit = 0  # 0-indexed, 0 = LSB

    with torch.no_grad():
        for _ in range(max_new):
            out = model(input_ids=input_ids, position_ids=position_ids)
            next_id = out.logits[0, -1].argmax().item()
            if next_id == NEWLINE_ID:
                break
            generated.append(next_id)
            # Compute next-token position-ID
            if is_digit_byte(next_id):
                next_pos = 1 + n_result_digit  # start=1
                n_result_digit += 1
            elif next_id == SPACE_ID:
                # Space inherits the position of the byte we just appended
                # to input_ids — which was the previous step's last output.
                next_pos = position_ids[0, -1].item()
            else:
                # Fallback (shouldn't happen for arithmetic outputs):
                next_pos = position_ids[0, -1].item() + 1

            # Append to running tensors
            input_ids = torch.cat([input_ids, torch.tensor([[next_id]], device=device)], dim=1)
            position_ids = torch.cat(
                [position_ids, torch.tensor([[next_pos]], device=device)], dim=1
            )
    return generated


def parse_lsb_first_to_int(decoded: str, expected_digit_count: int = None):
    """Decoded result string is LSB-first per-digit form (e.g. '0 0 1 7 4 4').
    Take only the LEADING contiguous run of single-digit tokens (separated
    by whitespace), reverse to MSB-first integer, return.

    The trained model may emit garbage after the result if it didn't learn
    EOS — we ignore everything after the first non-digit token. If
    `expected_digit_count` is given, take at most that many leading digit
    tokens (defends against an over-confident model that emits a 7-token
    answer when only 6 digits are expected, e.g., predicts a leading-zero
    tens'-place that pushes correct digits one slot off)."""
    s = decoded.strip()
    if not s:
        return None
    neg = False
    if s.startswith('-'):
        neg = True
        s = s[1:].strip()
    tokens = s.split()
    digit_run = []
    for t in tokens:
        if t.isdigit() and len(t) == 1:
            digit_run.append(t)
            if expected_digit_count is not None and len(digit_run) >= expected_digit_count:
                break
        else:
            break
    if not digit_run:
        return None
    msb_first = ''.join(reversed(digit_run))
    try:
        v = int(msb_first)
        return -v if neg else v
    except ValueError:
        return None


def main():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument('--sparrow', required=True, help='PC-trained Sparrow final/ dir')
    p.add_argument('--task', default='mul', choices=['add', 'sub', 'mul', 'div'])
    p.add_argument('--digits', type=int, default=3)
    p.add_argument('--n', type=int, default=50)
    p.add_argument('--seed', type=int, default=12345)
    p.add_argument('--max-new', type=int, default=32)
    p.add_argument('--report', default=None)
    p.add_argument('--device', default=None)
    args = p.parse_args()

    device = args.device or ('cuda' if torch.cuda.is_available() else 'cpu')
    print(f'  device: {device}')

    from transformers import Qwen3ForCausalLM
    print(f'  loading PC-trained Sparrow from {args.sparrow}')
    model = Qwen3ForCausalLM.from_pretrained(args.sparrow, torch_dtype=torch.float32)
    model.to(device); model.eval()

    op_char = {'add': '+', 'sub': '-', 'mul': '*', 'div': '/'}[args.task]

    # Build test set (use the same gen_problem logic as the existing eval —
    # NOT gen_problem_pc, since we want the EXPECTED answer in MSB-first form).
    rng = random.Random(args.seed)
    problems = []
    for _ in range(args.n):
        line = gen_problem(args.digits, op_char, rng).rstrip('\n')
        eq, ans_str = line.split(' = ')
        # Parse expected as int from MSB-first per-digit form
        ans_int = parse_lsb_first_to_int(' '.join(ans_str.split()[::-1]))  # reverse first then parse via the helper
        # Simpler: just collapse and int
        ans_collapsed = ans_str.replace(' ', '').replace(',', '')
        try:
            ans_int = int(ans_collapsed)
        except ValueError:
            ans_int = None
        problems.append((eq, ans_int, line))

    # Run inference
    n_correct = 0
    n_total = 0
    samples = []
    t_start = time.time()
    for prompt_lhs, ans_int, line in problems:
        # Parse a, b from the prompt's LHS
        toks = prompt_lhs.split()
        op_idx = next(i for i, t in enumerate(toks) if t in '+-*/')
        a = int(''.join(toks[:op_idx]))
        b = int(''.join(toks[op_idx + 1:]))
        prompt_ids, prompt_positions, _, _ = build_pc_prompt(a, b, op_char, args.digits)
        gen_bytes = generate_pc(model, prompt_ids, prompt_positions, device, args.max_new)
        decoded = decode(gen_bytes)
        # Parse decoded LSB-first to integer. We tell the parser the expected
        # digit count so over-emission garbage past the result doesn't poison.
        # For a*b with a,b in [10**(d-1), 10**d - 1], result is in [10**(2d-2), 10**(2d) - 1],
        # i.e. up to 2*d digits. We pass max(2*d, len_expected_digits).
        expected_digits = len(str(abs(ans_int))) if ans_int is not None else 2 * args.digits
        got_int = parse_lsb_first_to_int(decoded, expected_digit_count=expected_digits)
        match = (got_int is not None and ans_int is not None and got_int == ans_int)
        if match:
            n_correct += 1
        n_total += 1
        if len(samples) < 8:
            samples.append({
                'prompt': prompt_lhs + ' = ',
                'expected': ans_int,
                'got_decoded_lsb': decoded,
                'got_int': got_int,
                'match': match,
            })

    elapsed = time.time() - t_start
    accuracy = n_correct / max(n_total, 1)
    print(f'\n=== iter37 PC eval ({args.task} {args.digits}-digit) ===')
    print(f'  Sparrow-PC: {n_correct}/{n_total} = {accuracy*100:.1f}%')
    print(f'  elapsed: {elapsed:.1f}s')
    print(f'\n  sample outputs:')
    for s in samples:
        flag = 'OK   ' if s['match'] else 'WRONG'
        print(f"    [{flag}] prompt={s['prompt']!r}  exp={s['expected']}  got_lsb={s['got_decoded_lsb']!r}  got_int={s['got_int']}")

    if args.report:
        report = {
            'sparrow': args.sparrow,
            'task': args.task, 'digits': args.digits, 'n': args.n,
            'accuracy': accuracy, 'n_correct': n_correct, 'n_total': n_total,
            'elapsed_sec': elapsed,
            'samples': samples,
        }
        with open(args.report, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2, ensure_ascii=True)
        print(f'\n  report saved: {args.report}')


if __name__ == '__main__':
    main()
